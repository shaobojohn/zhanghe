package com.huaqin.android.market.sdk.bean;

public class ChargingInfo {
	
	private int chargeType;
	
	private String chargeChannel;
	
	private String chargeFactor;

	public int getChargeType() {
		return chargeType;
	}

	public void setChargeType(int chargeType) {
		this.chargeType = chargeType;
	}

	public String getChargeChannel() {
		return chargeChannel;
	}

	public void setChargeChannel(String chargeChannel) {
		this.chargeChannel = chargeChannel;
	}

	public String getChargeFactor() {
		return chargeFactor;
	}

	public void setChargeFactor(String chargeFactor) {
		this.chargeFactor = chargeFactor;
	}

}
